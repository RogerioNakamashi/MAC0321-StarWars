package Ex01;

public class Seres {
	private String Nome;
	private String Genero;
	private String Nascimento;
	private String PlanetaNatal;
	private float midiChlorians;
	
	public float getMidiChlorians() {
		return midiChlorians;
	}
	public void setMidiChlorians(float midiChlorians) {
		this.midiChlorians = midiChlorians;
	}
	public String getNome() {
		return this.Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getGenero() {
		return Genero;
	}
	public void setGenero(String genero) {
		Genero = genero;
	}
	public String getNascimento() {
		return Nascimento;
	}
	public void setNascimento(String nascimento) {
		Nascimento = nascimento;
	}
	public String getPlanetaNatal() {
		return PlanetaNatal;
	}
	public void setPlanetaNatal(String planetaNatal) {
		PlanetaNatal = planetaNatal;
	}
	
}
